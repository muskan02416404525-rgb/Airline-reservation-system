        private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());

        // Establish a database connection
        try {
            Connection connection = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);

            // SQL query to check if the username and password are correct
            String query1 = "SELECT * FROM login WHERE username = ? AND password = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query1);
            preparedStatement.setString(1, username);
            preparedStatement.setString(2, password);

            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                // Successful login
                JOptionPane.showMessageDialog(this, "Login successful");

            } else {
                // Wrong username or password
                JOptionPane.showMessageDialog(this, "Login failed. Please check your username and password.");
            }

            // Close the database connection
            resultSet.close();
            preparedStatement.close();
            connection.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while connecting to the database.");
        }

        }

